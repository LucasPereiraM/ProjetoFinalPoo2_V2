public class DadosPessoais {
    
    private String nome_crianca, nome_res, telefone;

    public String getNome_crianca() {
        return nome_crianca;
    }

    public void setNome_crianca(String nome_crianca) {
        this.nome_crianca = nome_crianca;
    }

    public String getNome_res() {
        return nome_res;
    }

    public void setNome_res(String nome_res) {
        this.nome_res = nome_res;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }
    
  
    
}
